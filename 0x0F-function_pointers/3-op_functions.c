#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: integer 1
 * @b: integer 2
 * Return: summition
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substracts two integers
 * @a: integer 1
 * @b: integer 2
 * Return: difference
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer 1
 * @b: integer 2
 * Return: multiplication
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides two integers
 * @a: integer 1
 * @b: integer 2
 * Return: division
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulus two integers
 * @a: integer 1
 * @b: integer 2
 * Return: remainder
 */

int op_mod(int a, int b)
{
return (a % b);
}
